import UIKit

extension UIView {
    
    func setColor() {
        self.backgroundColor = UIColor(red: 100, green: 50, blue: 109, alpha: 1)
    }
    
    func customColor() {
        self.backgroundColor = UIColor(red: 157/255, green: 248/255, blue: 179/255, alpha: 1)
    }
}
